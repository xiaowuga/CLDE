void BackgroundPass::initShader() {
    std::vector<char> vertexShaderCode = readFileFromAssets("shaders/background.vert");
    vertexShaderCode.push_back('\0');
    std::vector<char> fragmentShaderCode = readFileFromAssets("shaders/background.frag");
    fragmentShaderCode.push_back('\0');
    mShader.loadShader(vertexShaderCode.data(), fragmentShaderCode.data());

    mShader.use();
    mShader.setUniformInt("environmentMap", 0);

}

